const examples = [
  { path: '', name: 'Home Page' },
  { path: 'blog', name: 'Blog List' },
  { path: 'blog/web3-security-trends-2025', name: 'Blog Article' },
  { path: 'products/exchange', name: 'Exchange Product' },
];

examples.forEach(({ path, name }) => {
  console.log(`\n${name} (${path || '/'})`);
  console.log('-'.repeat(70));
  
  const links = generateHreflangLinks({ path });
  
  links.forEach(({ hreflang, href }) => {
    console.log(`<link rel="alternate" hreflang="${hreflang}" href="${href}" />`);
  });
});

